        C12.reset( new wdfTerminatedCap(1.0e-06,1) );
        CGK2.reset( new wdfTerminatedCap(1.600000e-12,1) );
        CGP2.reset( new wdfTerminatedCap(1.700000e-12,1) );
        CPK2.reset( new wdfTerminatedCap(4.600000e-13,1) );

        R14.reset( new wdfTerminatedRes(1000000) );

        E11.reset( new wdfTerminatedResVSource( 1.75e-3 * 100000 * -1 ,100000) );  //  Vs= Is * R and  sink current(-1). 
                                                                                   // More bigger R,  constant accuracy will improve.

        subtree4a.reset( new wdfTerminatedSeries( C10.get(), VIN1.get() ) );
        tree4.reset( new wdfTerminatedParallel( R10.get(), subtree4a.get() ) );
	    tree5.reset(new wdfTerminatedSeries( R11.get(), E21.get()) );
	    tree6.reset(new wdfTerminatedSeries( R12.get(), E22.get()) );


        // push pull
        // ...1st
        E31.reset( new wdfTerminatedResVSource( -55 * -1,1) );  // When use odd serial adaptor, negative voltage source turns to positive volatge. 
        C23.reset( new wdfTerminatedCap(100.0e-06,1) );
        R21.reset( new wdfTerminatedRes( 470000) );
        C21.reset( new wdfTerminatedCap( 4.700000e-08 ,1) );
        R25.reset( new wdfTerminatedRes(22) );

        subtree31a.reset(new wdfTerminatedParallel( E31.get(), C23.get()) );
