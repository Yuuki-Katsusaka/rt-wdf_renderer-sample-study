        R2T1.reset( new wdfTerminatedRes( 158400 ));
        R3T1.reset( new wdfTerminatedRes( 74.22736 ));
        C1T1.reset( new wdfTerminatedCap( 1126.0e-12,1) );
        L1T1.reset( new wdfTerminatedInd( 1.771e-3, 1) );
        L2T1.reset( new wdfTerminatedInd( 61.84, 1) );
        RLT1.reset( new wdfTerminatedRes( 2375.276 ));
        
    	subtree41a.reset(new wdfTerminatedSeries  (RLT1.get(), R3T1.get()));
	    subtree41b.reset(new wdfTerminatedParallel(R2T1.get(), L2T1.get()));
	    subtree41c.reset(new wdfTerminatedParallel(subtree41a.get(), subtree41b.get()));
	    subtree41d.reset(new wdfTerminatedSeries  (R1T1.get(), L1T1.get()));
	    subtree41e.reset(new wdfTerminatedSeries  (subtree41c.get(), subtree41d.get()));
