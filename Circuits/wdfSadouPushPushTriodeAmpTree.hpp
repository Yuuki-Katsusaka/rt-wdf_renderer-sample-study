        CGP1.reset( new wdfTerminatedCap(1.700000e-12,1) );
        CPK1.reset( new wdfTerminatedCap(4.600000e-13,1) );


        E22.reset( new wdfTerminatedResVSource(200 * -1 ,1) ); // When use odd serial adaptor, negative voltage source turns to positive volatge. 
